package basics;

import org.junit.jupiter.api.Test;

public class FloatTest {

    @Test
    void testFloatFamily() {
        // float double precision (64 bits)
        double x = 3.14;
        // float simple precision (32 bits)
        float xf = 3.14f;
        System.out.println(x);
        System.out.println(xf);
    }
}
